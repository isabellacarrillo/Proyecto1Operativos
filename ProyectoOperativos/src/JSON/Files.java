package JSON;

/**
 *
 * @author Admin
 */
public class Files {
    
    private int scripts;
    private int dubbings;
    private int animations;
    private int plotTwists;
    private int scenarios;
    private int deadline;
    private int dayDuration;
    private int ensambladores;
    
    public Files(int scripts, int dubbings, int animations, int plotTwists, int scenarios, int deadline, int dayDuration, int ensambladores){
    
        this.scripts = scripts;
        this.dubbings = dubbings;
        this.animations = animations;
        this.plotTwists = plotTwists;
        this.scenarios = scenarios;
        this.deadline = deadline;
        this.dayDuration = dayDuration;
        this.ensambladores= ensambladores;
    }
    
    
    
    

    /**
     * @return the scripts
     */
    public int getScripts() {
        return scripts;
    }

    /**
     * @param scripts the scripts to set
     */
    public void setScripts(int scripts) {
        this.scripts = scripts;
    }

    /**
     * @return the dubbings
     */
    public int getDubbings() {
        return dubbings;
    }

    /**
     * @param dubbings the dubbings to set
     */
    public void setDubbings(int dubbings) {
        this.dubbings = dubbings;
    }

    /**
     * @return the animations
     */
    public int getAnimations() {
        return animations;
    }

    /**
     * @param animations the animations to set
     */
    public void setAnimations(int animations) {
        this.animations = animations;
    }

    /**
     * @return the plotTwists
     */
    public int getPlotTwists() {
        return plotTwists;
    }

    /**
     * @param plotTwists the plotTwists to set
     */
    public void setPlotTwists(int plotTwists) {
        this.plotTwists = plotTwists;
    }

    /**
     * @return the scenarios
     */
    public int getScenarios() {
        return scenarios;
    }

    /**
     * @param scenarios the scenarios to set
     */
    public void setScenarios(int scenarios) {
        this.scenarios = scenarios;
    }

    /**
     * @return the deadline
     */
    public int getDeadline() {
        return deadline;
    }

    /**
     * @param deadline the deadline to set
     */
    public void setDeadline(int deadline) {
        this.deadline = deadline;
    }

    /**
     * @return the dayDuration
     */
    public int getDayDuration() {
        return dayDuration;
    }

    /**
     * @param dayDuration the dayDuration to set
     */
    public void setDayDuration(int dayDuration) {
        this.dayDuration = dayDuration;
    }

    /**
     * @return the ensambladores
     */
    public int getEnsambladores() {
        return ensambladores;
    }

    /**
     * @param ensambladores the ensambladores to set
     */
    public void setEnsambladores(int ensambladores) {
        this.ensambladores = ensambladores;
    }
    
    
}
